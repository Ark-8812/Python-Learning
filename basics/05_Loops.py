#Loops allow your code to keep running or on repeat without writing it many times

#A "while" loop allows the code to run while the condition is still true

count = 1 

while count  <= 5:
  print("Count is ", count)
  count = count + 1     #Increases count each time

#A "for" loop repeats a set number of times

for i in range(10):
  print("This is loop number", i) #You can use any letter no need to just put I
#The number in the brackets can also be changed 
